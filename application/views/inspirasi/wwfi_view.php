          var itemsWidth = 0;
          $('.list-pallet div').each(function(){
            var itemWidth = $(this).outerWidth();
            itemsWidth+=itemWidth;
          });
          return itemsWidth;
        };

        var widthOfHidden = function(){
          return (($('.row.mod-pallet').outerWidth())-widthOfList()-getLeftPosi())-scrollBarWidths;
        };

        var getLeftPosi = function(){
          return $('.list-pallet').position().left;
        };

        var reAdjust = function(){
          if (($('.row.mod-pallet').outerWidth()) < widthOfList()) {
            $('.scroller-right').show();
          }
          else {
            $('.scroller-right').hide();
          }
          
          if (getLeftPosi()<0) {
            $('.scroller-left').show();
          }
          else {
            $('.item').animate({left:"-="+getLeftPosi()+"px"},'slow');
            $('.scroller-left').hide();
          }
        }

        reAdjust();

        $(window).on('resize',function(e){  
            reAdjust();
        });

